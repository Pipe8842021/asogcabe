    <h1>¿QUIENES SOMOS?</h1>
    <h2>Asociación</h2>
    <p>ASOGCABE (Asociación de ganaderos y caballistas de Abejorral) es una asociación que desde nuestros inicios, nos hemos comprometido a proporcionar experiencias equinas excepcionales para jinetes de todos los niveles mediante este aplicativo web, el cual busca dinamizar los procesos de cabalgatas y otras actividades que giran en torno a la actividad equina, queremos impulsar el conocimiento y respeto hacia los equinos, así como promover la cultura y tradiciones del municipio recordando los recorridos de los colonizadores y ancestros que cruzaron los caminos y senderos a caballo para colonizar nuevas regiones del país como es el claro ejemplo para que se pudiese dar la colonización del departamento de caldas. </p>
    <p>ASOGCABE promueve el conocimiento, respeto, pasión y demás valores éticos hacia los caballos, con el fin de preservar los derechos de estos seres y proporcionando así una calidad de vida digna para estos. Aquí no solo ofrecemos servicios de equitación, sino que también nos esforzamos por crear una comunidad unida de amantes de los caballo, en ASOGCABE, creemos en la importancia de la conexión entre jinete y caballo, y nos esforzamos por fomentar esta relación en todas nuestras actividades, por esto es clave promover aquellas actividades y eventos relacionados con la actividad equina en el municipio de Abejorral, Aquí promovemos  a su vez el turismo en Abejorral, también podrás interactuar con los equinos sin importar que seas un novato, así sea simplemente para que encuentres un lugar donde compartir tu amor por los caballos con otros apasionados o simplemente aprender o empaparte del tema, ¡estás en el lugar correcto! </p>
    <p>En el aplicativo ASOGCABE tu puedes encontrar las diferentes actividades acerca de la actividad equina en el municipio de Abejorral, así como su día e información en relación a la actividad, en caso de que sea una cabalgata se podrá mostrar la ruta que se tomara en esta de igual manera como su hora de inicio, hipotética hora de finalización, su respectiva dificultad según la ruta y muy importante la reservación de los gallardetes y establos para la determinada cabalgata, de igual manera descripción y demás datos que sean necesarios a rededor de la cabalgata. </p>
